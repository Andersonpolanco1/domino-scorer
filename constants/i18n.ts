export function t(
  lang: Lang,
  key: keyof (typeof STRINGS)["es"],
  vars?: Record<string, string | number>,
): string {
  let str: string =
    (STRINGS[lang] as any)[key] ?? (STRINGS.es as any)[key] ?? key;
  if (vars) for (const k in vars) str = str.replace(`{${k}}`, String(vars[k]));
  return str;
}
